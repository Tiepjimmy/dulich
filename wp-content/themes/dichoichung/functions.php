function get_menu_name( $location ) {
    if( empty($location) ) return false;

    $locations = get_nav_menu_locations();
    if( ! isset( $locations[$location] ) ) return false;

    $menu_obj = get_term( $locations[$location], 'nav_menu' );

    return $menu_obj->name;
}

add_filter('query_vars', 'rlv_add_qv');
function rlv_add_qv($qv) {
    $qv[] = 'recently_booked';
    $qv[] = 'tour_destination';
    return $qv;
}

add_filter('relevanssi_hits_filter', 'relevanssi_colour_filter');
function relevanssi_colour_filter($hits) {
    global $wp_query;

    if (isset($wp_query->query_vars['recently_booked'])) {
        $correct_colour = array();
        foreach ($hits[0] as $hit) {
            $colour = get_field('recently_booked',$hit->ID);
            if ($colour == $wp_query->query_vars['recently_booked']) $correct_colour[] = $hit;
        }
        $hits[0] = $correct_colour;
    }
    return $hits;
}


add_filter('relevanssi_hits_filter', 'relevanssi_tour_destination_filter');
function relevanssi_tour_destination_filter($hits) {
    global $wp_query;

    if (isset($wp_query->query_vars['tour_destination'])) {
        $correct_colour = array();
        foreach ($hits[0] as $hit) {
            $tax_list = wp_get_post_terms($hit->ID, 'tour_destination', array("fields" => "ids"));
            foreach ($tax_list as $tax) {
                if ($tax == $wp_query->query_vars['tour_destination']) $correct_colour[] = $hit;
            }
        }
        $hits[0] = $correct_colour;
    }
    return $hits;
}


